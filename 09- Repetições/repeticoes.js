let valor = 8;

console.log("Valor inicial:", valor);

valor = valor + 1;

console.log("Somando 1 =", valor);

valor++;

console.log("soma =", valor);

valor--;

console.log('subtração =', valor);

valor -= 4;

console.log("subtração -4 =", valor);

valor *= 2;

console.log("multiplicação por 2 =", valor);

valor /= 2; 

console.log("divisão por 2 =", valor);